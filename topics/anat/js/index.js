function init() {
	const list = document.getElementById('questions'); 
	
	questionAnswers.forEach(item => {
		let option = document.createElement('option');
		option.value = item.question;
		list.appendChild(option);
	});
};

function searchAnswer() {
	var documentFrame = document.getElementById('documentFrame');
	var question = document.getElementById('question').value;
	
	questionAnswers.forEach(item => {
		if (item.question == question) {		
			documentFrame.src = "https://docs.google.com/viewer?srcid=" + item.answer + "&pid=explorer&efh=false&a=v&chrome=false&embedded=true";
			documentFrame.style.height='100%';
			documentFrame.style.visibility = null;		
		}
	});
}

function reset() {
	document.getElementById('question').value = '';
	var documentFrame = document.getElementById('documentFrame');
	documentFrame.style.height=0;
	documentFrame.style.visibility = 'hidden';
}