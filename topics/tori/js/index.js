function init() {
	const list = document.getElementById('questions'); 
	list.innerHTML = '';
	var combobox = document.getElementById('questionType');
	var value = combobox.options[combobox.selectedIndex].value;
	var questionAnswers;
	if (value == 'kahoot') {
		questionAnswers = kahootQuestionAnswers;
	} else {
		questionAnswers = summaryQuestionAnswers;
	}
	
	questionAnswers.forEach(item => {
		let option = document.createElement('option');
		option.value = item.question;
		list.appendChild(option);
	});
};

function searchAnswer() {
	const div = document.getElementById('answer');
	var combobox = document.getElementById('questionType');
	var value = combobox.options[combobox.selectedIndex].value;
	var questionAnswers;
	if (value == 'kahoot') {
		questionAnswers = kahootQuestionAnswers;
	} else {
		questionAnswers = summaryQuestionAnswers;
	}
	
	var question = document.getElementById('question').value;
	questionAnswers.forEach(item => {
		if (item.question == question) {
			document.getElementById('answer').innerHTML = item.answer;			
		}
	});
}

function reset() {
	document.getElementById('question').value = '';
	document.getElementById('answer').innerHTML = '';
}

function showPdf(fileId) {
	var pdfFrame = document.getElementById('pdfFrame');
	pdfFrame.src = "https://docs.google.com/viewer?srcid=" + fileId + "&pid=explorer&efh=false&a=v&chrome=false&embedded=true";
	pdfFrame.style.height='100%';
	pdfFrame.style.visibility = null;
}
function hidePdf() {
	var pdfFrame = document.getElementById('pdfFrame');
	pdfFrame.style.height=0;
	pdfFrame.style.visibility = 'hidden';
}